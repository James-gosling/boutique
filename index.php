  // FILTRO DE CATEGOR√çAS Y B√öSQUEDA EN EL CAT√ÅLOGO
  document.addEventListener('DOMContentLoaded', function () {
    const filterBtns = document.querySelectorAll('.filter-btn');
    const searchInput = document.getElementById('product-search');
    const productCards = document.querySelectorAll('#catalog-product-grid .product-card');
    const novedadesCards = document.querySelectorAll('.novedades-semana .product-card.novedad');
    const searchBtn = document.querySelector('.search-btn');
    const grid = document.getElementById('catalog-product-grid');

    function filterCatalog() {
      const searchTerm = (searchInput.value || '').toLowerCase();
      const selectedCategory = document.querySelector('.filter-btn.active').dataset.category;

      productCards.forEach(card => {
        const title = card.querySelector('h3,h4')?.textContent.toLowerCase() || '';
        const desc = card.querySelector('p')?.textContent.toLowerCase() || '';
        const category = card.getAttribute('data-category');
        const matchText = title.includes(searchTerm) || desc.includes(searchTerm);
        const matchCategory = (selectedCategory === 'todos') || (category === selectedCategory);

        if (matchText && matchCategory) {
          card.style.display = 'flex';
        } else {
          card.style.display = 'none';
        }
      });

      // Si selecciona Novedades, mostramos solo novedades de la semana tambi√©n en el cat√°logo
      if (selectedCategory === 'novedad') {
        novedadesCards.forEach(card => {
          // Si ya est√° en el cat√°logo general, no lo muevas
          if (!grid.contains(card)) {
            grid.prepend(card.cloneNode(true));
          }
        });
      } else {
        // Oculta novedades en grid principal si no son parte del cat√°logo
        Array.from(grid.children).forEach(card => {
          if (card.classList.contains('novedad')) {
            card.style.display = 'none';
          }
        });
      }
    }

    filterBtns.forEach(btn => {
      btn.addEventListener('click', function () {
        filterBtns.forEach(b => b.classList.remove('active'));
        this.classList.add('active');
        filterCatalog();
      });
    });

    searchInput.addEventListener('input', filterCatalog);
    searchBtn.addEventListener('click', filterCatalog);
  });
</script>
